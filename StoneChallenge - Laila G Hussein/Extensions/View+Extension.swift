import Foundation
import UIKit

extension UIView {
    
    func frameView() {
        
        self.layer.borderWidth = 2.5
        self.layer.borderColor = #colorLiteral(red: 0.4756349325, green: 0.4756467342, blue: 0.4756404161, alpha: 1)
    }
}
